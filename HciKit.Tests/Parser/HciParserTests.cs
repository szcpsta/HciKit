using HciKit.Parser;
using HciKit.Parser.Commands;

namespace HciKit.Tests.Parser;

public class HciParserTests
{
    [Fact]
    public void NotSupportedExceptionTest()
    {
        var parser = new HciParser();

        byte[] commandPacket = [0x1, 0x3, 0xc, 0x0];
        byte[] aclPacket =
        [
            0x2, 0x3, 0x0, 0x26, 0x0, 0x22, 0x0, 0x46, 0x0, 0x6, 0x0, 0x0, 0x0, 0x1d, 0x35, 0x11, 0x1c, 0x5e, 0x89,
            0x45, 0xb0, 0x95, 0x25, 0x11, 0xe3, 0xa5, 0xe2, 0x8, 0x0, 0x20, 0xc, 0x9a, 0x66, 0x3, 0xf0, 0x35, 0x5,
            0xa, 0x0, 0x0, 0xff, 0xff, 0x0
        ];
        byte[] eventPacket = [0x4, 0xe, 0x4, 0x1, 0x3, 0xc, 0x0];

        Assert.Throws<NotSupportedException>(() => parser.Parse(aclPacket));

        parser.Parse(commandPacket);
        parser.Parse(eventPacket);
    }
}
