using HciKit.Parser;
using HciKit.Parser.Events;

namespace HciKit.Tests.Parser;

public class HciParserTests
{
    [Fact]
    public void UnknownHciPacketTest()
    {
        var parser = new HciParser();

        byte[] aclPacket =
        [
            0x2, 0x3, 0x0, 0x26, 0x0, 0x22, 0x0, 0x46, 0x0, 0x6, 0x0, 0x0, 0x0, 0x1d, 0x35, 0x11, 0x1c, 0x5e, 0x89,
            0x45, 0xb0, 0x95, 0x25, 0x11, 0xe3, 0xa5, 0xe2, 0x8, 0x0, 0x20, 0xc, 0x9a, 0x66, 0x3, 0xf0, 0x35, 0x5,
            0xa, 0x0, 0x0, 0xff, 0xff, 0x0
        ];

        var result = parser.Parse(aclPacket) as UnknownHciPacket;

        Assert.NotNull(result);
        Assert.Equal(HciUnknownReason.UnsupportedPacketType, result.Reason);
    }

    [Fact]
    public void VendorSpecificEventTest()
    {
        var parser = new HciParser();
        byte[] packet = [0x4, 0xff, 0x14, 0x56, 0x4, 0x0, 0x0, 0x44, 0x50, 0x97, 0xc2, 0xe4, 0x10, 0x2, 0x80, 0xc0, 0x0, 0x0, 0x3, 0x2, 0x1, 0x2, 0x0];

        var vse = parser.Parse(packet) as HciEvent;

        Assert.NotNull(vse);
        Assert.True(vse.EventCode.IsVendorSpecific);
    }
}
